    <h3>Regeneration of body parts in salamanders</h3>
    
<p> Another exemple of animal with extraordinary regeneration capacity is the salamander. Salamender doesn't regenerate their all body from a head like flatworms, but they can regenerate a member after an amputation.<p>
    <video src="videos/Cell regeneration in the salamander.mp4" controls poster="videos/salamander.jpg" max-width="100%" min-width="100%" title="References: Nadia Rosenthal" preload="metadata"></video>
    <p> After an injury (partial or total loss of body structures) different process repair the injured zone, partially or totally.</p>

    <h4 id="inflammatory">Inflammatory response</h4>
    <p> The process begins with the inflammatory response where blood vessels dilate, allowing more blood to enter the injured zone. This in turn leads to an enhanced presence of white blood cells, neutrophils and lymphocytes, that enter the tissue and initiate the process of immune defense and tissue repair (figure 1). The ensuing inflammation is also responsable for the pain sensation, because macrophages produce chemical messengers that stimulate nociceptive nerves.</p>
