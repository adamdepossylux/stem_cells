    <h2> Regeneration in Animals</h2>

    <p>All animals are able to regenerate, but on different levels. After a modest injury (a bruise or a skin wound), you would expect your body to nearly completely repair the damaged tissue over a period one or two weeks : that’s regeneration. But a severe injury, like the loss of an arm, you normally wouldn’t expect much of your regenerative capacities, we accept that the arm is lost forever and look for engineers that produce an artificial device, a prosthesis, to replace the missing body part. The situation is quite different, and much more promising, for salamanders and planarian flatworms that have incredible regeneration skills. Below we will describe how they do it. This information may be helpful in determining how the regeneration process could be manipulated in humans.</p>
<br/>
    <p><strong>Problem: To what extent animals as planarian and salamanders are able to regenerate themselves ?</strong/></p>
<br/>

    <h3>Generation of body parts in planarian flatworms</h3>
    

    <p>Planarians are invertebrate flatworms that have the unbelievable ability to regenerate any part of their body. The most extraordinary example is the full regeneration of head structures after decapitation of the animal. More surprisingly, as a cherry on the cake, the animal also regains its memory.</p>
    
<p>The experience begin with an <span id="amputation">amputation</span>, in this picture decapitation, it is the first step of the process in order to understand mechanisms of planarian regeneration. Indeed we will see how planaria’s body reacts.</p>
<p>The accumulation of stem cells on the site of the injury forms what we call a <span id="blastema">blastema</span>. It is this cluster of cells that will reproduce, regenerate, the missing body part.</p>
<p>Stem cells of the blastema receive different kind of signals from nerve cells, provided by hormones. The same neurotransmitter is required in both processes but in order to <span id="regeneration_control">differentiate or to proliferate</span> different hormones are used. The one shown on the picture in blue induce an increase in the number of stem cells wheras the one in red order to the stem cell what lineage to adopt (turning into muscle, vasculature, nerve, epithelium, connective tissue, or others): what precursor cells to become. There are many different hormones such as serotonin and dopamine, some of them are produced by muscle cells.</p>
