    <p>Both innate and adapted immunity leads to elimination of micro-organism that have entered the wound.</p>
    <h4 id="unknown">Stem cells recruitment</h4>
    <p>The injury also causes the recruitment of Stem Cells, who are essential for the regeneration of the tissues.The mechanism responsible of Stem cell’s recruitment, globally referred to as the “injury signal” isn’t known yet, but may cytokines produced by the macrophages may play an important role (acting as chemo-attractants). Indeed, a studie performed by the Australian Regenerative Medecine Institute shows that the macrophages play a role in the regeneration of members for salamenders : they found that salamanders with macrophages being recruited into the injuried zone normally regenerate a new member in about one yearwhereas those that fail to recruit macrophages take much more longer to fully recover. Salamanders totally lacking macrophages do not regenerate at all; these animals just manage to heal the wound by covering the body part with a scar tissue.</p>
