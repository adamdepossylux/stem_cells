    <p> After an <a href="#amputation">amputation</a>, through a <a href="#unknown"> process still to be elucidated</a> but involving infiltrated <a href="#inflammatory"> macrophages</a>, stem cells are attracted to the site of the injury. They form stem cells <a href="#blastema">cluster</a> which, through <a href="#regeneration_control">control by locally generated hormones</a>, will differentiate into the different missing organs</p>

